number = 1 + 2 * 3 / 4.0
print(number)

# mod operator
remainder = 11 % 2
print(remainder)

# power
squared = 7 ** 2
cubed = 2 ** 3
print(squared)
print(cubed)


#ops with strings
helloworld = "hello" + " " + "world"
print(helloworld)

#duplicating strings
lotsofhellos = "hello" * 3
print(lotsofhellos)

#ops with lists
even_numbers = [2,4,6,8]
odd_numbers = [1,3,5,7]
all_numbers = odd_numbers + even_numbers # doesnt mix them in order. here, odd numbers listed first then even
print(all_numbers)


#Just as in strings, Python supports forming new lists with a repeating sequence using the multiplication operator
print([1,2,3] * 3)
